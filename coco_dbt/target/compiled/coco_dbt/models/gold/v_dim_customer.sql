

SELECT
    CUSTOMER_SK AS CUSTOMER_KEY,
    CUSTOMERNAME AS CUSTOMER_NAME,
    PHONE AS PHONE_NUMBER,
    ADDRESSLINE1 AS PRIMARY_ADDRESS,
    ADDRESSLINE2 AS SECONDARY_ADDRESS,
    CITY AS CITY_NAME,
    STATE AS STATE_PROVINCE,
    POSTALCODE AS POSTAL_CODE,
    COUNTRY AS COUNTRY_NAME,
    TERRITORY AS SALES_TERRITORY,
    CONTACTLASTNAME AS CONTACT_LAST_NAME,
    CONTACTFIRSTNAME AS CONTACT_FIRST_NAME
